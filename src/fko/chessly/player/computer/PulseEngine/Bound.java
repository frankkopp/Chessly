package fko.chessly.player.computer.PulseEngine;


final class Bound {

    static final int EXACT = 0;
    static final int UPPER = 1;
    static final int LOWER = 2;
    static final int NOBOUND = 3;

    static final int[] values = {
            EXACT, UPPER, LOWER
    };

    private Bound() {
    }

    static boolean isValid(int bound) {
        switch (bound) {
            case EXACT:
            case UPPER:
            case LOWER:
                return true;
            default:
                return false;
        }
    }

}
